'use strict'

const util = require('util')
const mysql = require('mysql')
const db = require('./../db')

module.exports = {
    get: (req, res) => {
        let sql = 'SELECT * FROM drinks'
        db.query(sql, (err, response) => {
            if (err) throw err
            res.json(response)
        })
    },
    detail: (req, res) => {
        let sql = 'SELECT * FROM drinks WHERE id = ?'
        db.query(sql, [req.params.drinkId], (err, response) => {
            if (err) throw err
            res.json(response[0])
        })
    },
    update: (req, res) => {
        let data = req.body;
        let drinkId = req.params.drinkId;
        let sql = 'UPDATE drinks SET ? WHERE id = ?'
        db.query(sql, [data, drinkId], (err, response) => {
            if (err) throw err
            res.json({message: 'Update success!'})
        })
    },
    store: (req, res) => {
        let data = req.body;
        let sql = 'INSERT INTO drinks SET ?'
        db.query(sql, [data], (err, response) => {
            if (err) throw err
            res.json({message: 'Insert success!'})
        })
    },
    delete: (req, res) => {
        let sql = 'DELETE FROM drinks WHERE id = ?'
        db.query(sql, [req.params.drinktId], (err, response) => {
            if (err) throw err
            res.json({message: 'Delete success!'})
        })
    }
}